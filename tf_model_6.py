"""### Hyperparameter Tuning for Random Forest Classifier

To enhance the performance of our Random Forest model, we will perform **hyperparameter tuning** using `GridSearchCV`. This method systematically works through multiple combinations of parameter tunes, cross-validating each combination to determine which set of parameters yields the best performance. This helps in optimizing the model and preventing overfitting.

"""

from sklearn.model_selection import GridSearchCV

print("Starting Hyperparameter Tuning for Random Forest...")

# Define the parameter grid to search
param_grid = {
    'n_estimators': [100, 200, 300],  # Number of trees in the forest
    'max_depth': [3, 5, 7],         # Maximum depth of the tree
    'min_samples_split': [2, 5, 10], # Minimum number of samples required to split an internal node
    'min_samples_leaf': [1, 2, 4]    # Minimum number of samples required to be at a leaf node
}

# Initialize GridSearchCV
# We use the 'accuracy' scoring for simplicity, but in financial modeling,
# other metrics like 'f1_weighted', 'roc_auc' might be more appropriate.
grid_search = GridSearchCV(
    estimator=RandomForestClassifier(random_state=42, class_weight='balanced'),
    param_grid=param_grid,
    scoring='accuracy',
    cv=3, # Using 3-fold cross-validation on the training set
    n_jobs=-1, # Use all available cores
    verbose=2
)

# Fit GridSearchCV to the training data
grid_search.fit(X_train, y_train)

print("Hyperparameter Tuning Complete.")
print(f"Best parameters found: {grid_search.best_params_}")
print(f"Best cross-validation accuracy: {grid_search.best_score_:.2f}")

# Get the best model
best_rf_model = grid_search.best_estimator_

# Evaluate the best model on the test data
rf_predictions_tuned = best_rf_model.predict(X_test)

print(f"\nRF Model Accuracy on Test Data (Tuned): {accuracy_score(y_test, rf_predictions_tuned):.2f}")


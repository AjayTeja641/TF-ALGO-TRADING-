print("Libraries imported successfully.")

# =========================================
# CELL 2: Download Data and calculate base returns
# =========================================
# CHANGE: Using NIFTYBEES.NS (Nifty 50 ETF) as a proxy for the Indian market.
# Note: Yahoo Finance data for Indian ETFs is generally reliable starting around 2010.
ticker = "NIFTYBEES.NS"
start_date = "2010-01-01"
end_date = "2025-11-28"

print(f"Downloading data for {ticker}...")
data = yf.download(ticker, start=start_date, end=end_date)

# Data integrity check
if data.empty or len(data) < 500:
    raise ValueError(f"Error: Not enough data downloaded for {ticker}. Try a different date range or ticker.")

# Determine the correct price column name, prioritizing ('Adj Close', ticker) then ('Close', ticker)
price_column_key = None

# Check for ('Adj Close', ticker) in MultiIndex
if isinstance(data.columns, pd.MultiIndex) and ('Adj Close', ticker) in data.columns:
    price_column_key = ('Adj Close', ticker)
# Fallback to ('Close', ticker) if ('Adj Close', ticker) is not found but it's a MultiIndex
elif isinstance(data.columns, pd.MultiIndex) and ('Close', ticker) in data.columns:
    price_column_key = ('Close', ticker)
# If not MultiIndex, assume flat columns and check for 'Adj Close'
elif 'Adj Close' in data.columns:
    price_column_key = 'Adj Close'
# Fallback to 'Close' if flat columns but 'Adj Close' not found
elif 'Close' in data.columns:
    price_column_key = 'Close'
else:
    raise ValueError(f"Error: Neither 'Adj Close' nor 'Close' could be identified in data columns for {ticker}. Available columns: {data.columns.tolist()}")

# Calculate Log Returns (better for modeling than simple percentage returns)
# We use the determined price_column_key to account for dividends and splits or use Close price
data['Log_Return'] = np.log(data[price_column_key] / data[price_column_key].shift(1))

# Drop the initial NaN created by the return calculation
data.dropna(inplace=True)

print(f"Data acquired for {ticker}. Shape: {data.shape}")
# Correctly reference 'Log_Return' using its MultiIndex key
print(data[[price_column_key, ('Log_Return', '')]].head())

# =========================================
# CELL 3: Advanced Feature Engineering
# =========================================
# Implementing the technical indicators promised in the PDF (RSI, MACD, Bands, ATR)

print("Generating Technical Indicators...")

# 1. Trend Indicators
data['SMA_50'] = ta.sma(data[price_column_key], length=50)
data['SMA_200'] = ta.sma(data[price_column_key], length=200)
# Ratio > 1 usually indicates an uptrend
data['SMA_Ratio'] = data['SMA_50'] / data['SMA_200']

# MACD
macd_df = ta.macd(data[price_column_key])
data['MACD'] = macd_df['MACD_12_26_9']

# 2. Momentum Indicators
data['RSI'] = ta.rsi(data[price_column_key], length=14)

# 3. Volatility Indicators
# Rolling Volatility (Annualized Standard Deviation)
data['Vol_20d'] = data[('Log_Return', '')].rolling(window=20).std() * np.sqrt(252)

# Bollinger Bands %B (Position relative to bands)
bbands_df = ta.bbands(data[price_column_key], length=20, std=2)
data['BB_PctB'] = (data[price_column_key] - bbands_df['BBL_20_2.0_2.0']) / (bbands_df['BBU_20_2.0_2.0'] - bbands_df['BBL_20_2.0_2.0'])

# ATR (Average True Range) - CRITICAL for risk management
data['ATR'] = ta.atr(data[('High', ticker)], data[('Low', ticker)], data[price_column_key], length=14)

# 4. Lagged Returns (Momentum persistence)
data['Lag1_Return'] = data[('Log_Return', '')].shift(1)

# Drop NaNs created by the rolling window calculations (e.g., SMA_200 needs 200 days of data)
data.dropna(inplace=True)

print("Feature Engineering Complete. New shape:", data.shape)

# =========================================
# CELL 4: Temporal Train/Test Split (Crucial for Legitimacy)
# =========================================
# IMPORTANT: We must split chronologically to prevent lookahead bias.
# We train on the past, test on the future.

# Use the first 80% of data for training, last 20% for out-of-sample testing.
split_index = int(len(data) * 0.80)

train_data = data.iloc[:split_index].copy()
test_data = data.iloc[split_index:].copy()

print(f"Training data range: {train_data.index.min().date()} to {train_data.index.max().date()} ({len(train_data)} days)")
print(f"Testing data range:  {test_data.index.min().date()} to {test_data.index.max().date()} ({len(test_data)} days)")

# =========================================
# CELL 5: HMM Regime Detection
# =========================================
# IMPORTANT: Fit the HMM ONLY on training data.

# Features used to define regimes (Returns and Volatility are best suited for this)
hmm_features = ['Log_Return', 'Vol_20d']

print("Training HMM on historical training data...")
# Using 3 components (e.g., Bull, Bear, Sideways/High Vol)
# n_iter increased for better convergence on complex data
hmm_model = GaussianHMM(n_components=3, covariance_type="full", n_iter=1000, random_state=42, tol=0.01)
hmm_model.fit(train_data[hmm_features])

# Check regime characteristics
print("\nRegime Means (Log Return, Volatility):")
# Regime with high mean return & low volatility is typically "Bull"
# Regime with negative mean return & high volatility is typically "Bear"
print(hmm_model.means_)

# Predict regimes for BOTH train and test sets using the fitted model
train_data['Regime'] = hmm_model.predict(train_data[hmm_features])
test_data['Regime'] = hmm_model.predict(test_data[hmm_features])

print("Regimes identified and assigned.")

# Optional: Visualize Regimes on Training Data
plt.figure(figsize=(14, 6))
colors = ['green', 'red', 'blue']
for i in range(hmm_model.n_components):
    mask = train_data['Regime'] == i
    plt.plot(train_data.index[mask], train_data[price_column_key][mask], '.', label=f'Regime {i}', color=colors[i] if i<3 else None, markersize=4)
plt.title(f'Market Regimes Identified by HMM (Training Data: {ticker})')
plt.ylabel('Price')
plt.legend()
plt.show()

# =========================================
# CELL 6: Machine Learning Signal Generation (Random Forest)
# =========================================

# 1. Define Target
# We predict if tomorrow's return will be positive (1) or negative (0).
# Shift returns back by 1 to align today's features with tomorrow's outcome.
train_data['Target'] = np.where(train_data['Log_Return'].shift(-1) > 0, 1, 0)
test_data['Target'] = np.where(test_data['Log_Return'].shift(-1) > 0, 1, 0)

# Drop the last row as it has no tomorrow
train_data.dropna(inplace=True)
test_data.dropna(inplace=True)

# 2. Define Features for the ML model
# Include technicals AND the identified Regime
feature_cols = ['SMA_Ratio', 'MACD', 'RSI', 'BB_PctB', 'ATR', 'Vol_20d', 'Lag1_Return', 'Regime']

X_train = train_data[feature_cols]
y_train = train_data['Target']
X_test = test_data[feature_cols]
y_test = test_data['Target']

# 3. Train RandomForest Classifier
print("Training Random Forest Classifier on training data...")
# class_weight='balanced' helps if bull/bear days are not evenly split
model_rf = RandomForestClassifier(n_estimators=200, max_depth=5, random_state=42, class_weight='balanced')
model_rf.fit(X_train, y_train)

# 4. Generate Predictions on (Unseen) Test Set
rf_predictions = model_rf.predict(X_test)

# Evaluation check (accuracy is less important than final strategy return)
print(f"\nRF Model Accuracy on Test Data: {accuracy_score(y_test, rf_predictions):.2f}")

# =========================================
# CELL 7: Risk Management & Position Sizing
# =========================================
# Implementing Volatility Targeting using ATR, as promised in the PDF.

# Ensure X_test and y_test are aligned with the current test_data
# (This is necessary if test_data was further dropped after initial X_test/y_test creation)
feature_cols = ['SMA_Ratio', 'MACD', 'RSI', 'BB_PctB', 'ATR', 'Vol_20d', 'Lag1_Return', 'Regime']
X_test_aligned = test_data[feature_cols]

# Re-generate predictions with the aligned X_test
rf_predictions_aligned = model_rf.predict(X_test_aligned)

# Add predictions back to test data
test_data['Signal'] = rf_predictions_aligned

# --- Volatility Targeting Strategy ---
# We aim for a constant annualized volatility of 15% for the portfolio.
target_vol_ann = 0.15

# Calculate current annualized volatility using ATR relative to price.
# ATR / Price roughly equals daily percentage volatility estimate.
current_vol_daily_est = test_data[('ATR', '')] / test_data[price_column_key]
current_vol_ann_est = current_vol_daily_est * np.sqrt(252)

# Calculate Position Scalar:
# If current market vol is higher than target (0.15), scalar < 1.0 (reduce size).
# If current market vol is lower than target, scalar > 1.0 (increase size).
# We clip leverage for safety: Min position 10%, Max leverage 1.5x.
position_scalar = (target_vol_ann / current_vol_ann_est).clip(lower=0.1, upper=1.5)

# IMPORTANT: Shift scalar by 1. We use yesterday's closing volatility to size today's trade.
test_data['Position_Size'] = position_scalar.shift(1)

# --- Calculate Strategy Returns ---
# Calculate simple percentage returns for realistic PnL calculation
test_data['Actual_Pct_Return'] = test_data[price_column_key].pct_change().shift(-1)

# Strategy Return = Signal (1 or 0) * Position Size * Tomorrow's Actual Return
# (If Signal is 0, return is 0 for that day).
test_data['Strategy_Return'] = test_data[('Signal', '')] * test_data[('Position_Size', '')] * test_data[('Actual_Pct_Return', '')]

# Fill NaNs from shifting
test_data.dropna(inplace=True)

print("Position sizing applied based on volatility monitoring.")
# Show recent examples of how position size changes
print(test_data[[price_column_key, ('ATR', ''), ('Position_Size', ''), ('Signal', '')]].tail(7))

# =========================================
# CELL 8: Performance Visualization (Out-of-Sample)
# =========================================

# Calculate Cumulative Returns using cumulative product of (1 + return)
test_data['Cumulative_BuyHold'] = (1 + test_data['Actual_Pct_Return']).cumprod()
test_data['Cumulative_Strategy'] = (1 + test_data['Strategy_Return']).cumprod()

# Plotting
plt.figure(figsize=(14, 8))
plt.plot(test_data.index, test_data['Cumulative_BuyHold'], label=f'Buy & Hold ({ticker})', color='grey', linestyle='--', alpha=0.7)
plt.plot(test_data.index, test_data['Cumulative_Strategy'], label='ML + HMM + Vol Target Strategy', color='navy', linewidth=2)

plt.title(f'Out-of-Sample Strategy Performance: {ticker} (Test Data Only)')
plt.ylabel('Cumulative Growth ($1 invested at start of test period)')
plt.xlabel('Date')
plt.legend(loc='upper left')
plt.grid(True, which='both', linestyle='--', linewidth=0.5)
plt.show()

# Final Performance Metrics Calculation
total_ret_bh = (test_data['Cumulative_BuyHold'].iloc[-1] - 1) * 100
total_ret_strat = (test_data['Cumulative_Strategy'].iloc[-1] - 1) * 100

# Annualized Volatility calculation
vol_bh = test_data['Actual_Pct_Return'].std() * np.sqrt(252) * 100
vol_strat = test_data['Strategy_Return'].std() * np.sqrt(252) * 100

# Sharpe Ratio estimate (assuming 0% risk-free rate for simplicity)
sharpe_strat = (test_data['Strategy_Return'].mean() * 252) / (test_data['Strategy_Return'].std() * np.sqrt(252))

print(f"\n--- Test Period Performance Summary ({test_data.index.min().date()} to {test_data.index.max().date()}) ---")
print(f"Buy & Hold ({ticker}): Total Return: {total_ret_bh:.2f}% | Annualized Vol: {vol_bh:.2f}%")
print(f"Strategy:             Total Return: {total_ret_strat:.2f}% | Annualized Vol: {vol_strat:.2f}%")
print(f"Strategy Sharpe Ratio: {sharpe_strat:.2f}")

print("--- STEP 1: TRAINING THE ML MODEL ---")

# 1. Download Training Data (Nifty 50 Index)
df_train = yf.download('^NSEI', start='2007-01-01', end='2024-01-01', progress=False)
if isinstance(df_train.columns, pd.MultiIndex): df_train.columns = df_train.columns.get_level_values(0)

# 2. Calculate 10 Advanced Features (The Inputs)
# Volatility
df_train['Volatility'] = df_train['Close'].pct_change().rolling(20).std()
df_train['ATR'] = ta.atr(df_train['High'], df_train['Low'], df_train['Close'], length=14)
bb = ta.bbands(df_train['Close'], length=20, std=2)
df_train['BB_Width'] = (bb['BBU_20_2.0_2.0'] - bb['BBL_20_2.0_2.0']) / bb['BBM_20_2.0_2.0']
# Momentum
df_train['RSI'] = ta.rsi(df_train['Close'], length=14)
df_train['ROC'] = ta.roc(df_train['Close'], length=10)
macd = ta.macd(df_train['Close'])
df_train['MACD'] = macd['MACD_12_26_9']
df_train['WillR'] = ta.willr(df_train['High'], df_train['Low'], df_train['Close'], length=14)
# Trend
adx = ta.adx(df_train['High'], df_train['Low'], df_train['Close'], length=14)
df_train['ADX'] = adx['ADX_14']
df_train['Dist_SMA50'] = (df_train['Close'] / ta.sma(df_train['Close'], length=50)) - 1
df_train['Dist_SMA200'] = (df_train['Close'] / ta.sma(df_train['Close'], length=200)) - 1

df_train.dropna(inplace=True)

# 3. Label the Regimes (The Answer Key)
# 2 = Bull (Up > 2.5%), 0 = Bear (Down > 2.5%), 1 = Sideways
df_train['Future_Ret'] = df_train['Close'].shift(-20) / df_train['Close'] - 1
def label_regime(x):
    if x > 0.025: return 2
    elif x < -0.025: return 0
    else: return 1
df_train['Target'] = df_train['Future_Ret'].apply(label_regime)

# 4. Train the Random Forest
features = ['Volatility', 'ATR', 'BB_Width', 'RSI', 'ROC', 'MACD', 'WillR', 'ADX', 'Dist_SMA50', 'Dist_SMA200']
rf_model = RandomForestClassifier(n_estimators=200, min_samples_leaf=5, random_state=42)
# Train on all available history excluding the undefined last 20 days
rf_model.fit(df_train[features].iloc[:-20], df_train['Target'].iloc[:-20])


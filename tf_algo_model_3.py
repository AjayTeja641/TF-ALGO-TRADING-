class PortfolioManager:
    def __init__(self, initial_cash=10000):
        self.cash = initial_cash
        self.initial_cash = initial_cash
        self.positions = {}
        self.transaction_log = []
        self.equity_curve = []
        self.prediction_log = [] # New: Log for predictions

    def _get_current_equity(self, current_prices):
        current_value = self.cash
        for ticker, qty in self.positions.items():
            if ticker in current_prices:
                current_value += qty * current_prices[ticker]
        return current_value

    def buy(self, date, ticker, price, quantity):
        cost = price * quantity
        if self.cash >= cost:
            self.cash -= cost
            self.positions[ticker] = self.positions.get(ticker, 0) + quantity
            self.transaction_log.append({
                'date': date,
                'ticker': ticker,
                'action': 'buy',
                'quantity': quantity,
                'price': price,
                'cost': cost
            })
            #print(f"{date.strftime('%Y-%m-%d')}: BUY {quantity} of {ticker} at {price:.2f}. Cash left: {self.cash:.2f}")
            return True
        #print(f"{date.strftime('%Y-%m-%d')}: FAILED BUY {quantity} of {ticker} at {price:.2f}. Insufficient cash. Cash: {self.cash:.2f}")
        return False

    def sell(self, date, ticker, price, quantity):
        if self.positions.get(ticker, 0) >= quantity:
            revenue = price * quantity
            self.cash += revenue
            self.positions[ticker] -= quantity
            if self.positions[ticker] == 0:
                del self.positions[ticker]
            self.transaction_log.append({
                'date': date,
                'ticker': ticker,
                'action': 'sell',
                'quantity': quantity,
                'price': price,
                'revenue': revenue
            })
            #print(f"{date.strftime('%Y-%m-%d')}: SELL {quantity} of {ticker} at {price:.2f}. Cash increased: {self.cash:.2f}")
            return True
        #print(f"{date.strftime('%Y-%m-%d')}: FAILED SELL {quantity} of {ticker} at {price:.2f}. No position. Position: {self.positions.get(ticker, 0)}")
        return False

    def close_position(self, date, ticker, price):
        if ticker in self.positions and self.positions[ticker] > 0:
            quantity = self.positions[ticker]
            return self.sell(date, ticker, price, quantity)
        return False

    def record_equity(self, date, current_prices):
        self.equity_curve.append({'date': date, 'equity': self._get_current_equity(current_prices)})

    def record_prediction(self, date, ticker, regime, confidence):
        self.prediction_log.append({
            'date': date,
            'ticker': ticker,
            'regime': regime,
            'confidence': confidence
        })

    def get_performance_metrics(self):
        final_equity = self.equity_curve[-1]['equity'] if self.equity_curve else self.initial_cash
        total_return = (final_equity - self.initial_cash) / self.initial_cash
        num_trades = len(self.transaction_log)

        # Calculate max drawdown (simplified)
        equity_series = pd.Series([d['equity'] for d in self.equity_curve], index=[d['date'] for d in self.equity_curve])
        if not equity_series.empty:
            rolling_max = equity_series.expanding().max()
            drawdown = (equity_series / rolling_max) - 1
            max_drawdown = drawdown.min()
        else:
            max_drawdown = 0.0

        return {
            'initial_cash': self.initial_cash,
            'final_equity': final_equity,
            'total_return': total_return,
            'num_trades': num_trades,
            'max_drawdown': max_drawdown
        }


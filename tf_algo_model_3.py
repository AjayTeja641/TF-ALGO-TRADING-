# Define a simple strategy based on our predictions
class MLRegimeStrategy(Strategy):
    def init(self):
        # Store predictions as part of the data
        # We need to align the predictions with the historical data correctly
        # For simplicity, we will assume the predictions are available at the start of the day.
        # In a real-world scenario, you would need careful handling of prediction timing.

        # Create a Series for predictions, indexed by date
        predictions_series = pd.Series(df_predictions.set_index('Ticker')['Prediction'])

        # For each ticker, we need to map its prediction to its historical data
        # This is a simplification; in a real-world scenario, predictions would likely be time-series based
        # and align directly with the 'data' DataFrame.

        # Here, we'll just use the most recent prediction for each stock throughout its history for demonstration.
        # A more complex strategy would involve dynamic predictions over time.
        self.prediction = self.I(lambda x: x, self.data.prediction, name='prediction')

    def next(self):
        # Get the current prediction for the stock
        current_prediction = self.prediction[-1]

        # Define trading logic based on predictions
        # 2 = Bullish (Go Long), 0 = Bearish (Go Short), 1 = Sideways (Stay Out)
        if current_prediction == 2: # Bullish
            if not self.position:
                self.buy() # Go long
        elif current_prediction == 0: # Bearish
            if not self.position:
                self.sell() # Go short
        else: # Sideways or other, close any open position
            if self.position:
                self.position.close()


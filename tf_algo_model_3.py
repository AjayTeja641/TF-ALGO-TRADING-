def calculate_features_for_new_data(data):
    # Ensure the columns are correctly formatted
    if isinstance(data.columns, pd.MultiIndex): data.columns = data.columns.get_level_values(0)

    # Calculate 10 Advanced Features (The Inputs)
    # Volatility
    data['Volatility'] = data['Close'].pct_change().rolling(20).std()
    data['ATR'] = ta.atr(data['High'], data['Low'], data['Close'], length=14)
    bb = ta.bbands(data['Close'], length=20, std=2)
    if bb is not None and not bb.empty and not bb['BBU_20_2.0_2.0'].isnull().all():
        data['BB_Width'] = (bb['BBU_20_2.0_2.0'] - bb['BBL_20_2.0_2.0']) / bb['BBM_20_2.0_2.0']
    else:
        data['BB_Width'] = np.nan

    # Momentum
    data['RSI'] = ta.rsi(data['Close'], length=14)
    data['ROC'] = ta.roc(data['Close'], length=10)
    macd = ta.macd(data['Close'])
    data['MACD'] = macd['MACD_12_26_9']
    data['WillR'] = ta.willr(data['High'], data['Low'], data['Close'], length=14)
    # Trend
    adx = ta.adx(data['High'], data['Low'], data['Close'], length=14)
    data['ADX'] = adx['ADX_14']
    data['Dist_SMA50'] = (data['Close'] / ta.sma(data['Close'], length=50)) - 1
    data['Dist_SMA200'] = (data['Close'] / ta.sma(data['Close'], length=200)) - 1

    data.dropna(inplace=True)

    if not data.empty:
        return data.iloc[-1]
    else:
        return pd.Series()


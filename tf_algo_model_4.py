def test_random_stock():
    # 1. Pick Random Stock
    ticker = random.choice(nifty_tickers)
    print(f"\n🎲 Analyzing Random Stock: {ticker}...")

    try:
        # 2. Get Live Data
        data = yf.download(ticker, period="1y", progress=False)
        if isinstance(data.columns, pd.MultiIndex): data.columns = data.columns.get_level_values(0)

        if len(data) < 60:
            print("   Not enough data. Retrying...")
            test_random_stock()
            return

        # 3. Calculate Features
        data = calculate_15_features(data)
        latest_row = data.iloc[-1]

        # 4. Predict
        features = latest_row[feature_cols].values.reshape(1, -1)
        pred = rf_model.predict(features)[0]
        prob = max(rf_model.predict_proba(features)[0]) * 100

        regimes = {0: "🐻 BEARISH (Cash/Sell)", 1: "🦀 SIDEWAYS (Wait)", 2: "🚀 BULLISH (Buy)"}

        # 5. Output
        print(f"---------------------------------------")
        print(f"💰 Price: ₹{latest_row['Close']:.2f}")
        print(f"📊 Regime: {regimes[pred]}")
        print(f"🧠 Confidence: {prob:.2f}%")
        print(f"---------------------------------------")
        print(f"   RSI: {latest_row['RSI']:.2f}")
        print(f"   ADX: {latest_row['ADX']:.2f}")
        print(f"   Vol: {latest_row['Volatility']:.4f}")
        print(f"---------------------------------------")

    except Exception as e:
        print(f"Error: {e}")


# Download Index Data (14 Years)
df_train = yf.download('^NSEI', start='2010-01-01', end='2025-11-28', progress=False)
if isinstance(df_train.columns, pd.MultiIndex): df_train.columns = df_train.columns.get_level_values(0)

# Apply Features
df_train = calculate_15_features(df_train)
print(f"✅ Training Data Loaded: {len(df_train)} days of Market History.")

print("--- STEP 5: TRAINING THE AI MODEL ---")

# 1. Create Targets (The "Truth")
df_train['Future_Ret'] = df_train['Close'].shift(-20) / df_train['Close'] - 1
def label_regime(x):
    if x > 0.025: return 2   # Bull
    elif x < -0.025: return 0 # Bear
    else: return 1            # Sideways
df_train['Target'] = df_train['Future_Ret'].apply(label_regime)

# 2. List the 15 Features used for input
feature_cols = ['Volatility', 'ATR', 'BB_Width', 'RSI', 'ROC', 'WillR', 'Stoch_K', 'CCI',
                'MACD', 'ADX', 'Dist_SMA50', 'Dist_SMA200', 'Dist_PSAR', 'MFI', 'CMF']

# 3. Train Random Forest
# Remove last 20 rows (NaN targets)
train_data = df_train.dropna()
rf_model = RandomForestClassifier(n_estimators=200, min_samples_leaf=5, random_state=42, n_jobs=-1)
rf_model.fit(train_data[feature_cols], train_data['Target'])

print("✅ Model Trained. The AI has learned market patterns.")

print("--- STEP 6: DEFINING STRATEGY LOGIC ---")

class MLRegimeStrategy(Strategy):
    def init(self):
        pass

    def next(self):
        # Read the 'Regime' column which we will generate using the AI
        regime = self.data.Regime[-1]

        if regime == 2: # Bull
            if not self.position:
                self.buy()
        elif regime == 0: # Bear
            if self.position:
                self.position.close()

print("✅ Strategy Logic Defined.")

print("--- STEP 7: RUNNING BACKTEST ON STOCKS ---")

results = []
count = 0

# Running on all stocks. This will take a considerable amount of time.
for ticker in nifty_tickers:
    try:
        # A. Get Data (5 Years)
        data = yf.download(ticker, period="5y", progress=False)
        if isinstance(data.columns, pd.MultiIndex): data.columns = data.columns.get_level_values(0)

        if len(data) < 500: continue # Skip young stocks

        # B. Calculate Indicators
        data = calculate_15_features(data)

        # C. AI Prediction (Vectorized for speed)
        # The AI looks at the 15 features for every row and decides the regime
        pred_input = data[feature_cols]
        data['Regime'] = rf_model.predict(pred_input)

        # D. Run Backtest
        bt = Backtest(data, MLRegimeStrategy, cash=100000, commission=0.002)
        stats = bt.run()

        results.append({
            'Ticker': ticker,
            'Return': stats['Return [%]'],
            'BuyHold': stats['Buy & Hold Return [%]'],
            'WinRate': stats['Win Rate [%]'],
            'Trades': stats['# Trades']
        })
        print(f"✅ {ticker}: {stats['Return [%]']:.1f}%")
        count += 1

    except Exception as e:
        print(f"❌ Error processing {ticker}: {e}")

print(f"✅ Completed testing on {count} stocks.")


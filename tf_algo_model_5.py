# 1. Downloading Training Data Nifty500 stocks
df_train = yf.download('^NSEI', start='2010-01-01', end='2024-01-01', progress=False)
if isinstance(df_train.columns, pd.MultiIndex): df_train.columns = df_train.columns.get_level_values(0)

# 2. Applying Features
df_train = add_market_intelligence(df_train)

# 3. Create Labels (The "Truth")
# Bull (2) = >2.5% return in 20 days
# Bear (0) = <-2.5% return in 20 days
# Sideways (1) = Between -2.5% and 2.5%
df_train['Future_Ret'] = df_train['Close'].shift(-20) / df_train['Close'] - 1
def label_regime(x):
    if x > 0.025: return 2
    elif x < -0.025: return 0
    else: return 1
df_train['Target'] = df_train['Future_Ret'].apply(label_regime)

# 4. Train Model
feature_cols = ['Volatility', 'ATR', 'BB_Width', 'RSI', 'ROC', 'WillR', 'Stoch_K', 'CCI',
                'MACD', 'ADX', 'Dist_SMA50', 'Dist_SMA200', 'Dist_PSAR', 'MFI', 'CMF']

clean_train = df_train.dropna()
rf_model = RandomForestClassifier(n_estimators=200, min_samples_leaf=5, random_state=42, n_jobs=-1)
rf_model.fit(clean_train[feature_cols], clean_train['Target'])


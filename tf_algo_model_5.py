def add_market_intelligence(data):
    """
    Calculates 15 Technical Indicators for ML Input.
    """
    df = data.copy()

    # --- A. VOLATILITY (Risk Assessment) ---
    # 1. Rolling Volatility (20-day standard deviation)
    df['Volatility'] = df['Close'].pct_change().rolling(20).std()
    # 2. ATR (Average True Range)
    df['ATR'] = ta.atr(df['High'], df['Low'], df['Close'], length=14)
    # 3. Bollinger Band Width (Squeeze detection)
    bb = ta.bbands(df['Close'], length=20, std=2)
    if bb is not None:
        df['BB_Width'] = (bb.iloc[:, 2] - bb.iloc[:, 0]) / bb.iloc[:, 1]

    # --- B. MOMENTUM (Price Velocity) ---
    # 4. RSI (Relative Strength Index)
    df['RSI'] = ta.rsi(df['Close'], length=14)
    # 5. ROC (Rate of Change)
    df['ROC'] = ta.roc(df['Close'], length=10)
    # 6. Williams %R
    df['WillR'] = ta.willr(df['High'], df['Low'], df['Close'], length=14)
    # 7. Stochastic Oscillator %K
    stoch = ta.stoch(df['High'], df['Low'], df['Close'])
    if stoch is not None: df['Stoch_K'] = stoch.iloc[:, 0]
    # 8. CCI (Commodity Channel Index)
    df['CCI'] = ta.cci(df['High'], df['Low'], df['Close'], length=20)

    # --- C. TREND (Direction Strength) ---
    # 9. MACD Line
    macd = ta.macd(df['Close'])
    if macd is not None: df['MACD'] = macd.iloc[:, 0]
    # 10. ADX (Trend Strength)
    adx = ta.adx(df['High'], df['Low'], df['Close'], length=14)
    if adx is not None: df['ADX'] = adx.iloc[:, 0]
    # 11. SMA 50 Distance (Medium term)
    df['Dist_SMA50'] = (df['Close'] / ta.sma(df['Close'], length=50)) - 1
    # 12. SMA 200 Distance (Long term)
    df['Dist_SMA200'] = (df['Close'] / ta.sma(df['Close'], length=200)) - 1
    # 13. Parabolic SAR Distance
    psar = ta.psar(df['High'], df['Low'], df['Close'])
    if psar is not None:
        psar_combined = psar.iloc[:, 0].fillna(0) + psar.iloc[:, 1].fillna(0)
        df['Dist_PSAR'] = (df['Close'] - psar_combined) / df['Close']

    # --- D. VOLUME (Conviction) ---
    # 14. MFI (Money Flow Index)
    df['MFI'] = ta.mfi(df['High'], df['Low'], df['Close'], df['Volume'], length=14)
    # 15. CMF (Chaikin Money Flow)
    df['CMF'] = ta.cmf(df['High'], df['Low'], df['Close'], df['Volume'], length=20)

    return df.dropna()

